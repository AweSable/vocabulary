function keyInterpret(e) {
  // console.log(e.key);
  switch (e.key){
    case 'F2':
      e.preventDefault();
      showAnswer();
      break;
    case 'F1':
      e.preventDefault();
      prevWord();
      break;
    case 'F3':
      e.preventDefault();
      nextWord();
      break;
    case 'F5':
      e.preventDefault();
      changeBookmarkState();
      break;
    case 'Enter':
      if (e.target != currentNumber) {
        e.preventDefault();
        if (answerText.innerHTML == '') {
          showAnswer();
          if (waitingTimeoutId) {
            clearTimeout(waitingTimeoutId);
            waitingTimeoutId = 0;
          }
        } else {
          nextWord();
        }
      }
      break;
    default:
      lastActivity = new Date().getTime();
      if (!event.ctrlKey && !event.altKey) {
        if (e.target != currentNumber & e.target != inputText & (inputText.innerHTML == '' || inputText.innerHTML == '<br>')) {
          inputText.focus();
        }
      }
      if (autoNextSelector) {
        clearTimeout(answerTimeoutId);
        answerTimeoutId = 0;
        if (waitingTimeoutId == 0) {
          waitingTimeoutId = setInterval(() => {
            if (new Date().getTime() - lastActivity > TIMEOUT) {
              showAnswer();
              clearTimeout(waitingTimeoutId);
              waitingTimeoutId = 0;
            }
          }, 1000);
        }
      }
  }
}

function userAnswerIsTrue() {
  const answerInLowerCase = getAnswer().toLowerCase().trim();
  answerInLowerCase.replace("’", "'");
  answerInLowerCase.replace("‘", "'");
  const inputInLowerCase = inputText.innerText.toLowerCase().trim();
  inputInLowerCase.replace("’", "'");
  inputInLowerCase.replace("‘", "'");
  return answerInLowerCase == inputInLowerCase;
}

